package com.example.corejava;

public class ShiftOperatorExample {
    public static void main(String[] args) {
        int i = 8 >> 1;
        int j = 2 << 1;
        int k = 8 >>> 1;
        System.out.println("i = " + i);
        System.out.println("j = " + j);
        System.out.println("k = " + k);
    }
}
